package Presentation;

/**
 * The enum Menu options.
 */
public enum MenuOptions {
    /**
     * Option that lists all the students stored in the system
     */
    CREATE_CHRACTER,

    /**
     * Option that validates the ticket of a student in the system
     */
    LIST_CHARACTERS,

    /**
     * Option that lets the user add a drink to a student using its ticket ID
     */
    CREATE_ADVENTURE,

    /**
     * Option that lets the user list all the students of a particular field in the system
     */
    START_ADVENTURE,

    /**
     * Option that exits the user from the application
     */
    EXIT,
}
